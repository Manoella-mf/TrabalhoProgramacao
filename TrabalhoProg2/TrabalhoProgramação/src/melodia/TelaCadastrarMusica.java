    public void cadastrarMusica() {
        //Validação
        criarConexao();
        if(!validarCampos()){
            exibirMensagem("Preencha todos os campos!");
            return;
        }
        //Obter os dados da música
        String album = (String) cbAlbum.getSelectedItem();
        String titulo = txtTitulo.getText();
        String data = dtData.getText();
        
        //Criar o aluno
        ManterMusica manter = new ManterMusica();
        manter.inserir(con, data, titulo, album, false);
        //Salvar o aluno BD
        
        //Exibir uma mensagem de sucesso
        exibirMensagem("Música cadastrado com sucesso!");
        
        //Limpar os dados da tela
        limparDados();
        encerrarConexao();
    } 
    
    public String buscaDados(){
        String artista = "";
        ArrayList<Conta> contas = TelaCadastrarConta.getContas();
        for(Conta a: contas){
            int tipoConta = a.getTipoConta();
            if(tipoConta == 1){
                artista = a.getNome();
            }
        }
        return artista;
    }
    
    public void listaAlbum(){
        criarConexao();
        ManterAlbum listaAlbum = new ManterAlbum();
        ArrayList<String> titulos = listaAlbum.listarAlbumArt(con, codigo);
        for(String titulo : titulos){
            cbAlbum.addItem(titulo);
        }
        encerrarConexao();
    }
    
    public static void criarConexao() {	
        ConexaoBancoDados conexao = new ConexaoBancoDados();
        try {
                con = conexao.getConexaoAcesso();
        }catch (Exception e) {
                System.out.println("Erro ao conectar o banco de dados: " + e.getMessage());
        }
    }
    public static void encerrarConexao() {
        ConexaoBancoDados conexao = new ConexaoBancoDados();
        try {
                con = conexao.close();
                System.out.println("Conexão com o Banco foi encerrada.....");
        }catch (Exception e) {
                System.out.println("Erro ao fechar a conexão: " + e.getMessage());
        }						
    }
    
    public void limparDados(){
        txtTitulo.setText("");
        dtData.setText("");
    }
    
    public void exibirMensagem(String msg){
        JOptionPane.showMessageDialog(null, msg);
    }
    
    public boolean validarCampos(){
        if(txtTitulo.getText().isEmpty() || dtData.getText().isEmpty())
            return false;
        return true;
    }
